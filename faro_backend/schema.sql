CREATE TABLE `SocialPost` (
  id INT NOT NULL AUTO_INCREMENT,
  userId INT NOT NULL,
  storyId INT NOT NULL,
  provider ENUM('X') NOT NULL,
  status ENUM('PENDING', 'POSTED', 'FAILED') NOT NULL DEFAULT 'PENDING',
  payload JSON NULL,
