CREATE TABLE `User` (
  id INT NOT NULL AUTO_INCREMENT,
  email VARCHAR(191) NOT NULL,
  name VARCHAR(191) NULL,
  password VARCHAR(191) NOT NULL,
  chatCredits DECIMAL(10,2) NOT NULL DEFAULT 10.00,
  plan ENUM('free','pro','max') NOT NULL DEFAULT 'free',
  planRenewsAt DATETIME NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY User_email_key (email)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `UserProfile` (
  id INT NOT NULL AUTO_INCREMENT,
  userId INT NOT NULL,
  businessName VARCHAR(191) NULL,
  stage ENUM('IDEA', 'EARLY_REVENUE', 'GROWTH', 'SCALING') NOT NULL DEFAULT 'IDEA',
  industry VARCHAR(191) NULL,
  relocationWindow VARCHAR(191) NULL,
  budgetRange VARCHAR(191) NULL,
  priorities JSON NULL,
  currentLocation VARCHAR(191) NULL,
  relocationNotes TEXT NULL,
  responseStyle VARCHAR(191) NULL,
  language VARCHAR(191) NULL DEFAULT 'en',
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY UserProfile_userId_key (userId),
  CONSTRAINT UserProfile_userId_fkey FOREIGN KEY (userId) REFERENCES `User`(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `Session` (
  id INT NOT NULL AUTO_INCREMENT,
  userId INT NOT NULL,
  token VARCHAR(191) NOT NULL,
  ipAddress VARCHAR(191) NULL,
  userAgent VARCHAR(191) NULL,
  expiresAt DATETIME NOT NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY Session_token_key (token),
  KEY Session_userId_idx (userId),
  CONSTRAINT Session_userId_fkey FOREIGN KEY (userId) REFERENCES `User`(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `PasswordResetToken` (
  id INT NOT NULL AUTO_INCREMENT,
  userId INT NOT NULL,
  tokenHash CHAR(64) NOT NULL,
  expiresAt DATETIME NOT NULL,
  usedAt DATETIME NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  KEY PasswordResetToken_userId_idx (userId),
  UNIQUE KEY PasswordResetToken_tokenHash_key (tokenHash),
  CONSTRAINT PasswordResetToken_userId_fkey FOREIGN KEY (userId) REFERENCES `User`(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

